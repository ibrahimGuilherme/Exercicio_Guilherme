n1 = input('Digite algo: ')
print(f'Numeric: {n1.isnumeric()}\n'
      f'Alpha: {n1.isalpha()}\n'
      f'AlphaNum: {n1.isalnum()}\n'
      f'Decimal: {n1.isdecimal()}\n'
      f'Digit: {n1.isdigit()}\n'
      f'Lower: {n1.islower()}\n'
      f'Upper: {n1.isupper()}\n'
      f'Printable: {n1.isprintable()}\n'
      f'Space: {n1.isspace()}\n'
      f'Title: {n1.istitle()}')